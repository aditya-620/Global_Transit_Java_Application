    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Add new Vehicle
        try{
            String v_no = vno.getText();
            String name = n.getText();
            String category = (String)c.getSelectedItem();
            String s = (String)d.getSelectedItem();
            StringTokenizer st = new StringTokenizer(s,":");
            String d_name = st.nextToken();
            String d_phone = st.nextToken();

            db.DbConnect.st.executeUpdate("insert into vehicle (v_no,name,category,driver_name,driver_phone) values('"+v_no+"','"+name+"','"+category+"','"+d_name+"','"+d_phone+"')");
            JOptionPane.showMessageDialog(null,"Success!");
            getEntries();
        }catch(SQLIntegrityConstraintViolationException ex){ //for primary key exception
            JOptionPane.showMessageDialog(null,"Entry Already exist!");
        }catch(Exception ex){  //sql exception
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // DELETE ROW
        try{
            int ri = jTable1.getSelectedRow();
            if(ri == -1){
                JOptionPane.showMessageDialog(null,"Please Select Row!");
            } else {
                String p = (String)jTable1.getValueAt(ri, 0);
                db.DbConnect.st.executeUpdate("delete from vehicle where v_no='"+p+"'");
                JOptionPane.showMessageDialog(null,"Success!");
                getEntries();
            }
        }catch(Exception ex){  //sql exception
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Update Driver to Vehicle:
        try{
            
            String v_no = (String)v.getSelectedItem();
            String s = (String)dd.getSelectedItem();
            StringTokenizer st = new StringTokenizer(s,":");
            String d_name = st.nextToken();
            String d_phone = st.nextToken();

            db.DbConnect.st.executeUpdate("update vehicle set driver_name='"+d_name+"',driver_phone='"+d_phone+"',v_no='"+v_no+"' ");
            JOptionPane.showMessageDialog(null,"Success!");
            getEntries();
        }catch(SQLIntegrityConstraintViolationException ex){ //for primary key exception
            JOptionPane.showMessageDialog(null,"Entry Already exist!");
        }catch(Exception ex){  //sql exception
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

