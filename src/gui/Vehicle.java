    private void getEntries(){
        //there is a class name tablemodel with child named defaulttablemodel
        //and we are doing down casting here
        javax.swing.table.DefaultTableModel dtm =(javax.swing.table.DefaultTableModel) jTable1.getModel();
        
        //remove rows from JTable
        int rc = dtm.getRowCount();
        for(int a = 1; a<rc; a++){
            dtm.removeRow(0);
        }
        //add rows from DB to Jtable
        try{
            ResultSet rs = db.DbConnect.st.executeQuery("select * from vehicle");
            
            //we are adding data to the database here
            while(rs.next()){
                String v = rs.getString("v_no");
                String n = rs.getString("name");
                String c = rs.getString("category");
                String dn = rs.getString("driver_name");
                String dp = rs.getString("driver_phone");
                
                java.util.Vector r = new java.util.Vector();
                r.add(v);
                r.add(n);
                r.add(c);
                r.add(dn);
                r.add(dp);
                dtm.addRow(r);
            }    
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
        }
    }

